let toggleOn1 = document.querySelector(".toggle_on1")
let toggleOff1 = document.querySelector(".toggle_off1")
toggleOn1.style.display = "none"
function toggleBtn1() {
   if (toggleOn1.style.display == "none") {
      toggleOn1.style.display = "block"
      toggleOff1.style.display = "none"
   } else {
      toggleOn1.style.display = "none"
      toggleOff1.style.display = "block"
   }
}
